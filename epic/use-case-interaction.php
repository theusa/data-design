<h1>Use Case/Interaction Flow for Amazon</h1>
<ul>
    <li>Precondition: Have an amazon account, have her address and credit card information in the system.</li>
    <li>Becky would like to buy alcohol from Amazon and have it delivered to her home.</li>
    <li>Becky visits <a href = www.amazon.com>Amazon</a>.</li>
    <li>Becky presses the log in button</li>
    <li>Amazon returns a sign in sheet prompting her for username and password</li>
    <li>Becky enters an incorrect password</li>
    <li>Amazon prompts her to try again</li>
    <li>Becky enters her password, this time correctly</li>
    <li>Amazon logs her in, and personalizes her home page</li>
    <li>Becky searches for her favorite types of alcohol which are:</li>
        <ul>
            <li>Sky Vodka</li>
            <li>Wild Turkey</li>
            <li>Any wine from California</li>
        </ul>
    <li>Amazon displays the items she searched for</li>
    <li>Becky adds those items to her cart by pressing the "Add to Cart" button</li>
    <li>Amazon adds the items becky pressed to her cart</li>
    <li>Becky goes through the checkout process and pays for her items</li>
    <li>Amazon coordinates with their delivery branch to ship the product to Becky</li>
</ul>

